/**
 * 빌드 시점에 data/*.md(지식베이스)를 코드 모듈로 인라인한다.
 *  → src/lib/rag/kb.generated.ts 생성
 *
 * [왜] 서버리스(Vercel 등) 배포에서 런타임 `fs.readFileSync`는 파일 추적 누락으로
 *      KB가 함수 번들에 안 들어갈 수 있다(실제로 한국어 KB가 누락된 사고가 있었음).
 *      KB를 JS 문자열로 박아넣으면 번들의 일부가 되어 어디서든 100% 안전하다.
 *
 * npm 스크립트의 predev/prebuild/eval 에서 자동 실행된다.
 */
import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const dataDir = path.join(root, "data");
const outFile = path.join(root, "src", "lib", "rag", "kb.generated.ts");

const files = fs
  .readdirSync(dataDir)
  .filter((f) => f.endsWith(".md"))
  .sort();

const entries = files.map((name) => ({
  name,
  content: fs.readFileSync(path.join(dataDir, name), "utf-8"),
}));

const out =
  `// AUTO-GENERATED by scripts/bundle-kb.mjs — 직접 수정하지 마세요.\n` +
  `// data/*.md 를 빌드 시점에 번들로 인라인합니다(서버리스 배포 안전).\n` +
  `export const knowledgeFiles: { name: string; content: string }[] =\n` +
  `${JSON.stringify(entries)};\n`;

fs.writeFileSync(outFile, out, "utf-8");
console.log(
  `[bundle-kb] ${entries.length}개 KB 인라인: ${files.join(", ")} → ${path.relative(root, outFile)}`,
);
